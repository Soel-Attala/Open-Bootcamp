//OPERADORES
//Operadores aritméticos
// + - * / % ++ --
int i = 0 ;
Console.WriteLine("Suma: "+(i+1));
Console.WriteLine("Autoincremento: "+ i++);
Console.WriteLine("Division: "+(i/2));
//con la division si el resultado es decimal, nos devuelve el entero mas cercano, por ejemplo 5/2 = 2
//si es 0.5 nos devuelve 0, si es 1.5 nos devuelve 1, etc.
Console.WriteLine("Autodecremento: "+ --i);
Console.WriteLine("Resta: "+(i-1));
Console.WriteLine("Modulo: "+(i%2));
//modulo nos devuelve el resto de la division, por ejemplo 5%2 = 1



